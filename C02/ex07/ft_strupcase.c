char	*ft_strupcase(char *str)
{
	int	id;
	int	d;

	d = 'a' - 'A';
	id = 0;
	while (str[id] != '\0')
	{
		if (str[id] >= 'a' && str[id] <= 'z')
		{
			str[id] -= d;
		}
		id++;
	}
	return (str);
}
